    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
       
        if(i >= 2){
            JOptionPane.showMessageDialog(null,"Lista de clientes cheia!");
            txtId.setText("");
            txtNome.setText("");
            txtEmail.setText("");
            txtTelefone.setText("");
            txtId.requestFocus();
            return;
        }
            if(txtId.getText().equals("")){
                JOptionPane.showMessageDialog(null,"E necessario preencher o campo ID para realizar o cadastro!");
                return;
            }else if(txtNome.getText().equals("")){
                JOptionPane.showMessageDialog(null,"E necessario preencher o campo Nome para realizar o cadastro!");
                return;
            }else if(txtEmail.getText().equals("")){
                JOptionPane.showMessageDialog(null,"E necessario preencher o campo Email para realizar o cadastro!");
                return;
            }else if(txtTelefone.getText().equals("")){
                JOptionPane.showMessageDialog(null,"E necessario preencher o campo Telefone para realizar o cadastro!");
                return;
            }else {
                try{
                   
                    c = new Cliente();    
                    c.setId(Integer.parseInt(txtId.getText()));
                    c.setNome(txtNome.getText());
                    c.setEmail(txtEmail.getText());
                    c.setTel(Integer.parseInt(txtTelefone.getText()));
                    cli[i] = c;
                    i++;
                    
                    JOptionPane.showMessageDialog(null,"Cadastrado com sucesso");
                }catch(NumberFormatException exception){
                    JOptionPane.showMessageDialog(null,"No campo 'ID' e 'TELEFONE' deve ser digitado somente numeros!");
                }
            }
            txtId.setText("");
            txtNome.setText("");
            txtEmail.setText("");
            txtTelefone.setText("");
        
            txtId.requestFocus();
    }//GEN-LAST:event_btnCadastrarActionPerformed

